package Tricky_Questions;

public class Single_quote_print_statement {
    public static void main(String[] args) {
//        it will add ascii values of all character present
//        in print statement according to ascii value numbering
        System.out.println('j'+'a'+'v'+'a');
//        it will print java as it is
        System.out.println("j"+"a"+"v"+"a");
//        it will add ascii values of 'j','a','v'
//        because it is in single quotes 'a' will be printed as it is
        System.out.println('j'+'a'+'v'+"a");
    }
}
